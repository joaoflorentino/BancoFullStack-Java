package jflorentino;

public class SegundaAtividade {
	/** Atividade curso Full Stack UFSC */
	private int identRobo;
	private int coodX;
	private int coorY;
	
	//Introduz getrs e setrs
	public int getIdentRobo() {
		return identRobo;
	}
	public void setIdentRobo(int identRobo) {
		this.identRobo = identRobo;
	}
	public int getCoodX() {
		return coodX;
	}
	public void setCoodX(int coodX) {
		this.coodX = coodX;
	}
	public int getCoorY() {
		return coorY;
	}
	public void setCoorY(int coorY) {
		this.coorY = coorY;
	}
	//Construtor 
	public SegundaAtividade(int identRobo, int coodX, int coorY) {
		super();
		this.identRobo = identRobo;
		this.coodX = coodX;
		this.coorY = coorY;
	}
	
	
	
	
	
	
}
